// Helper function to get counters for specific equipment
export function getEquipmentCounters(equipmentName: string) {
  const item = equipment.find(e => e.name === equipmentName);
  return item?.counters || null;
}

// Helper function to find what items this equipment counters
export function whatDoesThisItemCounter(equipmentName: string): string[] {
  return equipment
    .filter(item => item.counters.items.includes(equipmentName))
    .map(item => item.name);
}

